package eu.orionos.build.configGenerator;

import java.io.BufferedWriter;
import java.io.File;
import java.io.FileWriter;
import java.io.IOException;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Comparator;
import java.util.Set;
import java.util.SortedSet;

import eu.orionos.build.Config;
import eu.orionos.build.Module;
import eu.orionos.build.Semantics;

/**
 * @author bemk
 *
 */
public class DefHeader {
	private Module modules;
	private DepFile dep_file;

	private static String license_hdr = "/* THIS FILE HAS BEEN GENERATED AUTOMATICALLY,\n"
			+ " * DO NOT EDIT THIS FILE MANUALLY. ALL MODIFICATIONS WILL\n"
			+ " * BE OVERWRITTEN. */ \n"
			+ "\n\n"
			+ "/* This file observes the license used by the project */\n"
			+ "/* In the case of more than one conflicting license, this\n"
			+ " * file is property of the public domain */\n";

	private static String C_hdr = "#ifndef __BUILD_DEF_HDR__\n"
			+ "#define __BUILD_DEF_HDR__\n\n"
			+ "#ifdef __cplusplus\nextern(\"C\") {\n#endif\n";

	private static String C_ftr = "#ifdef __cplusplus\n}\n#endif\n\n#endif\n\n";

	public DefHeader(Module modules, DepFile dep_file) {
		this.modules = modules;
		this.dep_file = dep_file;
	}

	public void write() {
		String def_hdr_path = dep_file.getDefHeader();
		if (def_hdr_path == null || def_hdr_path.equals("")) {
			return;
		}
		/* Prepare the headers and sort them */
		Set<String> hdrs = modules.get_def_hdr();
		ArrayList<String> sorted_headers = new ArrayList<String>(hdrs);
		Collections.sort(sorted_headers);

		/* Try to write the file */
		try {
			File def_file = new File(def_hdr_path);
			if (def_file.exists() && !def_file.isDirectory()) {
				def_file.delete();
			}
			FileWriter defines = new FileWriter(def_file);

			defines.write(license_hdr);
			defines.write("\n\n");
			defines.write(C_hdr);
			defines.write("\n");

			for (String s : sorted_headers) {
				defines.write(s);
			}

			defines.write(C_ftr);

			defines.close();

		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}
}
